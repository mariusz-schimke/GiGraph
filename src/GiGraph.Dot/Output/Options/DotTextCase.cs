namespace GiGraph.Dot.Output.Options;

/// <summary>
///     Text case options.
/// </summary>
public enum DotTextCase
{
    /// <summary>
    ///     Uses the default text casing.
    /// </summary>
    Default,

    /// <summary>
    ///     Converts text to upper case.
    /// </summary>
    Upper,

    /// <summary>
    ///     Converts text to lower case.
    /// </summary>
    Lower
}